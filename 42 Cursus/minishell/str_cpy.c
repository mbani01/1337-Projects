#include "shell.h"

void	str_cpy(char **dst, char **src)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (src[0][i])
	{
		if (src[0][i] == '~')
		{
			i++;
			continue ;
		}
		dst[0][j++] = src[0][i++];
	}
	src[0][j] = '\0';
}
